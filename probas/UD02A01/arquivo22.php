<?php
$var1 = 4.5;
$var2 = 5;

//Suma
$resultado = $var1 + $var2; //resultado: 9.5

//Resta
$resultado = $var1 - $var2; //resultado: -0.5
echo 'Resultado é: '.$resultado;

//Multiplicación
$resultado = $var1 * $var2; //resultado: 22.5
echo 'Resultado é: '.$resultado;

//División
$resultado = $var1 / $var2; //resultado: 0.9
echo 'Resultado é: '.$resultado;

//Módulo (considera só a parte enteira das variables
$resultado = $var1 % $var2; //resultado: 4
echo 'Resultado é: '.$resultado;
$var1 = -10;
$var2 = 3.98;
$resultado = $var1 % $var2; //resultado: -1
echo 'Resultado é: '.$resultado;
?>
